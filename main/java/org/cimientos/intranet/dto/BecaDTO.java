package org.cimientos.intranet.dto;

import org.cimientos.intranet.modelo.Beca;

public class BecaDTO {

	private Beca beca;
	private int cantAlumnos;
	
	
	public BecaDTO(Beca b, int cant){
		super();
		beca = b;
		cantAlumnos = cant;
	}
	
	public Beca getBeca() {
		return beca;
	}
	public void setBeca(Beca beca) {
		this.beca = beca;
	}
	public int getCantAlumnos() {
		return cantAlumnos;
	}
	public void setCantAlumnos(int cantAlumnos) {
		this.cantAlumnos = cantAlumnos;
	}
		
}